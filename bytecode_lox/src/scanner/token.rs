use lox_proc_macros::U8Enum;

#[derive(Clone, Debug)]
pub struct Token<'scanner> {
    pub kind: TokenKind,
    pub lexeme: &'scanner str,
    pub line: usize,
}

#[derive(Debug, Clone, Copy, PartialEq, U8Enum)]
#[repr(u8)]
pub enum TokenKind {
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Comma,
    Dot,
    Minus,
    Plus,
    Semicolon,
    Slash,
    Star,

    // One or two character tokens.
    Bang,
    BangEqual,
    Equal,
    EqualEqual,
    Greater,
    GreaterEqual,
    Less,
    LessEqual,

    // Literals.
    Identifier,
    String,
    Number,

    // Keywords.
    And,
    Class,
    Else,
    False,
    Fun,
    For,
    If,
    Nil,
    Or,
    Print,
    Return,
    Super,
    This,
    True,
    Var,
    While,

    Error,
    Eof,
}
